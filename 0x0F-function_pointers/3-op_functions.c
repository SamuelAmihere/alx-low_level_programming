#include "3-calc.h"

/**
 * op_add - performs addition
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs division
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs modulo
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_mod(int a, int b)
{
	return (a % b);
}
