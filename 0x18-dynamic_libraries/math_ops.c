#include "math_ops.h"

/**
 * add - performs addition
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - performs subtraction
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - performs multiplication
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - performs division
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - performs modulo
 *
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int mod(int a, int b)
{
	return (a % b);
}
